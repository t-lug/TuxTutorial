def greet(name):
    """Greet the user with their name."""
    return f"Hello, {name}! Welcome to the Git and GitHub workshop."

def add_numbers(a, b):
    """Add two numbers and return the result."""
    return a + b

if __name__ == "__main__":
    print(greet("Participant"))
    print("Adding 5 and 3:", add_numbers(5, 3))
